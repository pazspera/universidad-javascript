// Concatenar cadenas
var nombre = 'Juan';
var apellido = 'Perez';

var nombreCompleto = nombre + ' ' + apellido;
console.log(nombreCompleto);

var nombreCompleto2 = 'Carlos' + ' ' + 'Gomez';
console.log(nombreCompleto2);

var x = nombre + 2 + 4;
console.log(x);
console.log(typeof x);

x = nombre + (2 + 4);
console.log(x);

x = 2 + 4 + nombre;
console.log(x);
console.log(typeof x);
